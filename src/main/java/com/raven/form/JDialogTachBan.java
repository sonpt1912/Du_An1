    private void showDataBanMoi(List<Ban> listBan) {
        dtmBanMoi.setRowCount(0);
        for (int i = 0; i < listBan.size(); i++) {
            dtmBanMoi.addRow(new Object[]{listBan.get(i).getMaBan(), listBan.get(i).getKv().getTenKV(), listBan.get(i).getSoLuongChoNgoi(), listBan.get(i).getTrangThai()});
        }
    }

    private boolean checkMaTrung() {
        boolean check = true;
        listBan = banService.getFull();
        for (int i = 0; i < listBan.size(); i++) {
            if (Integer.valueOf(txtMaBanMoi.getText()) == listBan.get(i).getMaBan()) {
                return false;
            } else {
                check = true;
                return check;
            }
        }
        return check;
    }

