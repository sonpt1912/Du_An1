    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int index = tbXepLoai.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "?");
        } else {
            if (txtMaRank.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Mã không được để trống");
                return;
            }
            if (txtTenRank.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Mã không được để trống");
                return;
            }
            if (!txtTenRank.getText().matches("[^\\s][A-Za-zÀÁÂÃÈÉÊÌÍÒÓÔÕÙÚÝàáâãèéêìíòóôõùúýĂăĐđĨĩŨũƠơƯưẠ-ỹ ]+[^\\s]")) {
                JOptionPane.showMessageDialog(this, "Tên không đúng định dạng");
                return;
            } else {
                int ma = Integer.valueOf(txtMaRank.getText());
                RankKhachHang rankKhachHang = newRank();

                JOptionPane.showMessageDialog(this, rankServiceImpl.update(rankKhachHang, ma));
                listRank = rankServiceImpl.getAll();
                showDataTable(listRank);
                btnClearActionPerformed(evt);
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed
    public boolean kiemTraTrungMaLoai() {
        listRank = rankServiceImpl.getAll();
        for (RankKhachHang l : listRank) {
            if (txtMaRank.getText().equalsIgnoreCase((l.getMaRank()))) {
                return false;
            }
        }
        return true;
    }

    private RankKhachHang newRank() {
        RankKhachHang rankKhachHang = new RankKhachHang();
        rankKhachHang.setMaRank(txtMaRank.getText());
        rankKhachHang.setTenRank(txtTenRank.getText());
        rankKhachHang.setSoTienBatDau(BigDecimal.valueOf(Double.valueOf(txtSotienBatDau.getText())));
        rankKhachHang.setSoTienKetThuc(BigDecimal.valueOf(Double.valueOf(txtSoTienKThuc.getText())));
        rankKhachHang.setKhuyenMaiRank(Integer.valueOf(txtSoPhanTramGiam.getText()));
        if (radioActive.isSelected()) {
            rankKhachHang.setTrangThai(0);
        } else {
            rankKhachHang.setTrangThai(1);
        }
        return rankKhachHang;
    }
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (!txtMaRank.getText().isEmpty()) {
            RankKhachHang loai = newRank();
            if (!(kiemTraTrungMaLoai())) {
                JOptionPane.showMessageDialog(this, "Mã đã tồn tại");
                return;

            }
            if (txtMaRank.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Mã không được để trống");
                return;
            }
            if (txtTenRank.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Mã không được để trống");
                return;
            }
            if (!txtTenRank.getText().matches("[^\\s][A-Za-zÀÁÂÃÈÉÊÌÍÒÓÔÕÙÚÝàáâãèéêìíòóôõùúýĂăĐđĨĩŨũƠơƯưẠ-ỹ ]+[^\\s]")) {
                JOptionPane.showMessageDialog(this, "Tên không đúng định dạng");
                return;
            } else {
                JOptionPane.showMessageDialog(this, rankServiceImpl.add(loai));
                listRank = rankServiceImpl.getAll();
                showDataTable(listRank);
                btnClearActionPerformed(evt);
            }
        } else {
            JOptionPane.showMessageDialog(this, "clear trước khi add");
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int index = tbXepLoai.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "?");
        } else {
            int ma = Integer.valueOf(txtMaRank.getText());
            JOptionPane.showMessageDialog(this, rankServiceImpl.remove(ma));
            listRank = rankServiceImpl.getAll();
            showDataTable(listRank);
            btnClearActionPerformed(evt);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

