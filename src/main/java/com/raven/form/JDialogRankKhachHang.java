    private void tbXepLoaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbXepLoaiMouseClicked
        // TODO add your handling code here:
        RankKhachHang rankKhachHang = listRank.get(tbXepLoai.getSelectedRow());
        txtMaRank.setText(String.valueOf(rankKhachHang.getMaRank()));
        txtTenRank.setText(rankKhachHang.getTenRank());
        txtSoTienKThuc.setText(String.valueOf(rankKhachHang.getSoTienKetThuc()));
        txtSotienBatDau.setText((String.valueOf(rankKhachHang.getSoTienBatDau())));
        txtSoPhanTramGiam.setText(String.valueOf(rankKhachHang.getKhuyenMaiRank()));
    }//GEN-LAST:event_tbXepLoaiMouseClicked
    private void showDataTable(List<RankKhachHang> listRank) {
        dtmRank.setRowCount(0);
        for (int i = 0; i < listRank.size(); i++) {
            dtmRank.addRow(new Object[]{listRank.get(i).getMaRank(), listRank.get(i).getTenRank(), listRank.get(i).getSoTienBatDau(),
                listRank.get(i).getSoTienKetThuc(), listRank.get(i).getKhuyenMaiRank(), listRank.get(i).getTrangThai()});
        }
    }

