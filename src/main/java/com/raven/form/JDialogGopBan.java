    private void btnGopActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGopActionPerformed
        // TODO add your handling code here:
        //Bàn click
        String ma = lbMaBan.getText();
        Ban bans = (Ban) banService.getOne(ma);
        // Bàn được chọn từ cbb
        String ma1 = cbbBan.getSelectedItem().toString();
        Ban bancbb = (Ban) banService.getOne(ma1);
        int gopBan = JOptionPane.showConfirmDialog(null, "Xác nhận gộp bàn");
        if (gopBan == JOptionPane.NO_OPTION) {
            return;
        } else if (gopBan == JOptionPane.CLOSED_OPTION) {
            return;
        } else if (gopBan == JOptionPane.CANCEL_OPTION) {
            return;
        } else {
            bans.setSoLuongChoNgoi(bans.getSoLuongChoNgoi() + bancbb.getSoLuongChoNgoi());
            banService.update(bans, ma);
            bancbb.setSoLuongChoNgoi(0);
            bancbb.setTrangThai(2);
            banService.update(bancbb, ma1);
            JOptionPane.showMessageDialog(this, "Gộp Thành Công");
            this.dispose();
        }
    }//GEN-LAST:event_btnGopActionPerformed

