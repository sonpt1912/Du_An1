    private void loadCbb() {
        listBan = banService.getAllTrangThai1();
        for (int i = 0; i < listBan.size(); i++) {
            if (listBan.get(i).getMaBan().toString().equals(lbMaBan.getText())) {
                listBan.remove(i);
            }
        }
        for (Ban ban1 : listBan) {
//            dcbm.addElement(ban1.getMaBan() + "  Số lượng chỗ (" + ban1.getSoLuongChoNgoi() + ")");
            dcbm.addElement(ban1.getMaBan());
        }
    }

